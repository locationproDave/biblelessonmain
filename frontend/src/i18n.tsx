// Context
interface I18nContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: string, params?: Record<string, string | number>) => string
  dir: 'ltr' | 'rtl'
  languageInfo: LanguageInfo
}

const I18nContext = createContext<I18nContextType | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  // Force English only - language switching disabled
  const [language] = useState<Language>('en')

  const setLanguage = (_lang: Language) => {
    // Language switching disabled - English only
  }

  useEffect(() => {
    document.documentElement.dir = 'ltr'
    document.documentElement.lang = 'en'
  }, [])

  const t = (key: string, params?: Record<string, string | number>): string => {
    let text = translations.en[key] || key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      })
    }
    return text
  }

  const languageInfo = LANGUAGES[0] // English

  const contextValue = { language, setLanguage, t, dir: 'ltr' as const, languageInfo }

  return (
    <I18nContext.Provider value={contextValue}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}

// Language name helper for AI prompt
export function getLanguageFullName(code: Language): string {
  return LANGUAGES.find(l => l.code === code)?.name || 'English'
}
